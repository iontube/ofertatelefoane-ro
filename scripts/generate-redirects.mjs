// Affiliate cloaking. MAIN page curated phones -> static public/_redirects.
// PHONES feed -> a Pages Function functions/out/[slug].js mapping every offer's cloak key -> deeplink
// (bare slug = cheapest offer; slug~merchant = that merchant's offer). Dropped models -> 301 to similar.
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

// curated main-page /out -> profitshare deeplinks (committed source; products.json affiliate is just "/out/<slug>")
let curated = [];
try { curated = readFileSync(fileURLToPath(new URL('../src/data/main-out.txt', import.meta.url)), 'utf-8').trim().split('\n').filter(Boolean); } catch { /* none */ }
let redirects = '';
let mainN = 0;
for (const line of curated) {
  const [path, url, code = '302'] = line.trim().split(/\s+/);
  if (!path || !url) continue;
  redirects += `${path} ${url} ${code}\n`;
  redirects += `${path}/ ${url} ${code}\n`;   // slash variant (data-o opens /out/<slug>/)
  mainN++;
}
const main = { length: mainN };
let dropped = {};
try { dropped = JSON.parse(readFileSync(fileURLToPath(new URL('../.cache/dropped.json', import.meta.url)), 'utf-8')); } catch { /* none */ }
let dn = 0;
for (const [slug, target] of Object.entries(dropped)) { redirects += `/telefon/${slug}/ ${target} 301\n`; redirects += `/telefon/${slug} ${target} 301\n`; dn++; }
const ruleCount = redirects.split('\n').length - 1;
if (ruleCount > 1800) console.warn(`  WARN: _redirects ${ruleCount} rules (cap 2000) — lower RETAIN_DAYS.`);
writeFileSync(fileURLToPath(new URL('../public/_redirects', import.meta.url)), redirects);
console.log(`_redirects: ${main.length} main + ${dn} dropped 301s (${ruleCount} rules)`);

let recs = [];
try { recs = JSON.parse(readFileSync(fileURLToPath(new URL('../src/data/telefoane.json', import.meta.url)), 'utf-8')); } catch { /* none yet */ }
const map = {};
// curated main-page /out (the Function intercepts ALL /out/*, so curated slugs MUST be in the MAP too)
for (const line of curated) { const [path, url] = line.trim().split(/\s+/); if (path && url) { const s = path.replace(/^\/out\//, '').replace(/\/$/, ''); map[s] = url; } }
for (const p of recs) for (const o of (p.offers || [])) if (o.outKey && o.affiliate && !map[o.outKey]) map[o.outKey] = o.affiliate;

mkdirSync(fileURLToPath(new URL('../functions/out', import.meta.url)), { recursive: true });
const fn = `// AUTO-GENERATED by scripts/generate-redirects.mjs — cloaked affiliate redirect for /out/<key>/
const MAP = ${JSON.stringify(map)};
export function onRequest(context) {
  const url = MAP[context.params.slug];
  if (url) return Response.redirect(url, 302);
  return Response.redirect(new URL('/oferte/', context.request.url).toString(), 302);
}
`;
writeFileSync(fileURLToPath(new URL('../functions/out/[slug].js', import.meta.url)), fn);
console.log(`functions/out/[slug].js: ${Object.keys(map).length} offer deeplinks`);
